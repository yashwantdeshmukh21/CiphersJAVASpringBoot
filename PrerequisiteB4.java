//Accept two booleans and perform all logical operations (AND, OR, NOT)

class LogicalOperations {
    public static void main(String[] args) {
        boolean p = true;
        boolean q = false;
        System.out.println("AND: " + (p && q));
        System.out.println("OR: " + (p || q));
        System.out.println("NOT p: " + (!p));
        System.out.println("NOT q: " + (!q));
    }
}
